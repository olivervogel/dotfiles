return {
		
	-- -----------------------------------------------------------------------
	-- console log
	-- -----------------------------------------------------------------------

	s("cl", fmt([[
		console.log({});
	]], {
		i(1)
	})),

	-- -----------------------------------------------------------------------
	-- function
	-- -----------------------------------------------------------------------

	s("fun", fmt([[
	function {name}({args}) {{
		{body}
	}}
	]], {
		name = i(1, "functionName"),
		args = i(2, "arguments"),
		body = i(0, "//")
	})),

	s("fn", fmt([[
	({args}) => {{
		{body}
	}}
	]], {
		args = i(1, "arguments"),
		body = i(0, "//")
	})),

	-- -----------------------------------------------------------------------
	-- class
	-- -----------------------------------------------------------------------

	s("class", fmt([[
	class {name} {{
		constructor() {{
			{body}
		}}
	}}
	]], {
		name = i(1, "ClassName"),
		body = i(0, "//")
	})),

	-- -----------------------------------------------------------------------
	-- return
	-- -----------------------------------------------------------------------

	s("ret", {
		t("return "),
		i(0),
		t(";"),
	}),

	s("ret1", t("return true;")),
	s("ret0", t("return false;")),

	-- -----------------------------------------------------------------------
	-- comment bar
	-- -----------------------------------------------------------------------

	s("bar", fmt([[
	/* {line} */
	/*  {title}  */
	/* {line} */
	]], {
		title = i(1),
		line = f(function(args, snip)
			return string.rep("-", args[1][1]:len() + 2) end, 1
		),
	})),
}
