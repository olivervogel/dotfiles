return {

	-- -----------------------------------------------------------------------
	-- header
	-- -----------------------------------------------------------------------

	s_inline("e", fmt([[
	<%= {value} %>
	]], {
		value = i(0),
	})),

	-- -----------------------------------------------------------------------
	-- if
	-- -----------------------------------------------------------------------

	s("if", fmt([[
	<%= if {statement} do %>
		{body}
	<% end %>
	]], {
		statement = i(1, "statement"),
		body = i(0),
	})),

	-- -----------------------------------------------------------------------
	-- render
	-- -----------------------------------------------------------------------

	s("render", fmt([[
	<%= render "{template}", assigns %>
	]], {
		template = i(0),
	})),
	
	-- -----------------------------------------------------------------------
	-- for
	-- -----------------------------------------------------------------------

	s("for", fmt([[
	<%= for {item} <- {items} do %>
		{body}
	<% end %>
	]], {
		item = i(1, "item"),
		items = i(2, "items"),
		body = i(0)
	})),

	-- -----------------------------------------------------------------------
	-- dgettext
	-- -----------------------------------------------------------------------

	s("gt", {
		t("<%= "),
		t("dgettext("),
		t("\""),
		i(2, "group"),
		t("\", \""),
		i(1, "text"),
		t("\") %>"),
	}),

	s("slot", fmt([[
	<%= render_slot(@{inner_block}) %>
	]], {
		inner_block = i(0)
	})),
}
