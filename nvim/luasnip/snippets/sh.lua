return {

	-- -----------------------------------------------------------------------
	-- if
	-- -----------------------------------------------------------------------

	s("if", fmt([[
	if [ {condition} ]; then
		{body}
	fi
	]], {
		condition = i(1),
		body = i(0),
	})),

	-- -----------------------------------------------------------------------
	-- fun
	-- -----------------------------------------------------------------------

	s("fun", fmt([[
	{name}() {{
		{body}
	}}
	]], {
		name = i(1, "newFunction"),
		body = i(0),
	})),

}
