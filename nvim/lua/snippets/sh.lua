return {

	-- -----------------------------------------------------------------------
	-- if
	-- -----------------------------------------------------------------------

	s("if", fmt([[
	if [ {condition} ]; then
		{body}
	fi
	]], {
		condition = i(1),
		body = i(0),
	})),

	-- -----------------------------------------------------------------------
	-- fun
	-- -----------------------------------------------------------------------

	s("fun", fmt([[
	{name}() {{
		{body}
	}}
	]], {
		name = i(1, "newFunction"),
		body = i(0),
	})),

	-- -----------------------------------------------------------------------
	-- case
	-- -----------------------------------------------------------------------

	s("case", fmt([[
	case {word} in
		pattern)
			# pattern
		;;
		*)
			# default
		;;
	esac
	]], {
		word = i(0, "$1"),
	})),
	-- -----------------------------------------------------------------------
	-- printf
	-- -----------------------------------------------------------------------

	s("pr", {
		t("printf \""),
		i(0),
		t("\""),
	})

}
