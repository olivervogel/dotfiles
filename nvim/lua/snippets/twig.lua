return {

	-- -----------------------------------------------------------------------
	-- echo
	-- -----------------------------------------------------------------------
	s_inline("e", { 
		t("{{ "),
		i(0, ""),
		t(" }}"),
	}),

	s_inline("ee", { 
		t("{{ "),
		i(0, "value"),
		t("|raw }}"),
	}),

	-- -----------------------------------------------------------------------
	-- if
	-- -----------------------------------------------------------------------
	s("if", fmt([[
   {{% if {condition} %}}
   {body}
   {{% endif %}}
	]], {
		condition = i(1, "condition"),
		body = i(0),
	})),

	-- -----------------------------------------------------------------------
	-- block
	-- -----------------------------------------------------------------------
	s("block", fmt([[
   {{% block {name} %}}
   {body}
   {{% endblock %}}
	]], {
		name = i(1, "name"),
		body = i(0),
	})),

	-- -----------------------------------------------------------------------
	-- extend block
	-- -----------------------------------------------------------------------
	s("ext", { 
		t("{% extends \""),
		i(0, "name"),
		t(".html.twig\" %}"),
	}),

	-- -----------------------------------------------------------------------
	-- for
	-- -----------------------------------------------------------------------
	s("for", fmt([[
   {{% for {item} in {collection} %}}
   {body}
   {{% endfor %}}
	]], {
		item = i(1, "item"),
		collection = i(2, "collection"),
		body = i(0),
	})),

	-- -----------------------------------------------------------------------
	-- include
	-- -----------------------------------------------------------------------
	s("inc", fmt([[
   {{{{ include('{name}.html.twig') }}}}
	]], {
		name = i(0),
	})),

	-- -----------------------------------------------------------------------
	-- set
	-- -----------------------------------------------------------------------
	s("set", fmt([[
   {{% set {key} = {value} %}}
	]], {
		key = i(1, 'key'),
		value = i(0, 'value'),
	})),
}
