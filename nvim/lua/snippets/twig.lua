return {

	-- -----------------------------------------------------------------------
	-- echo
	-- -----------------------------------------------------------------------
	s_inline("e", { 
		t("{{ "),
		i(0, ""),
		t(" }}"),
	}),

	s_inline("ee", { 
		t("{{ "),
		i(0, "value"),
		t("|raw }}"),
	}),

	-- -----------------------------------------------------------------------
	-- if
	-- -----------------------------------------------------------------------
	s("if", fmt([[
   {{% if {condition} %}}
      {body}
   {{% endif %}}
	]], {
		condition = i(1, "condition"),
		body = i(0),
	})),

	-- -----------------------------------------------------------------------
	-- block
	-- -----------------------------------------------------------------------
	s("block", fmt([[
   {{% block {name} %}}{body}{{% endblock %}}
	]], {
		name = i(1, "name"),
		body = i(0),
	})),

	-- -----------------------------------------------------------------------
	-- extend block
	-- -----------------------------------------------------------------------
	s("ext", { 
		t("{% extends \""),
		i(0, "name"),
		t(".html.twig\" %}"),
	}),
}
