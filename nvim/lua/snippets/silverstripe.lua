return {

	-- -----------------------------------------------------------------------
	-- echo
	-- -----------------------------------------------------------------------

	s_inline("e", { 
		t("$"),
		i(0, "Variable")
	}),

	s_inline("et", { 
		t("<%t "),
		i(1, "Namespace"),
        t("."),
		i(2, "Entity"),
        t(" \""),
		i(3, "Translation"),
        t("\""),
        t(" %>"),
	}),
	-- -----------------------------------------------------------------------
	-- loop
	-- -----------------------------------------------------------------------
	
	s("loop", fmt([[
	<% loop ${var} %>
		{body}
	<% end_loop %>
	]], {
		var = i(1, "Variable"),
		body = i(0),
	})),	

	-- -----------------------------------------------------------------------
	-- with
	-- -----------------------------------------------------------------------

	s("with", fmt([[
	<% with ${var} %>
		{body}
	<% end_with %>
	]], {
		var = i(1, "Variable"),
		body = i(0),
	})),
	
	-- -----------------------------------------------------------------------
	-- if
	-- -----------------------------------------------------------------------

	s("if", fmt([[
	<% if ${condition} %>
		{body}
	<% end_if %>
	]], {
		condition = i(1, "Condition"),
		body = i(0),
	})),

	s_inline("iff", fmt([[
	<% if ${condition} %>{body}<% end_if %>
	]], {
		condition = i(1, "Condition"),
		body = i(0),
	})),

	-- -----------------------------------------------------------------------
	-- include
	-- -----------------------------------------------------------------------
	
	s("inc", {
		t("<% include "),
		i(1, "Template"),
		t(" %>"),
	})

}
